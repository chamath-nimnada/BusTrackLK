/// Small utility holder for commonly used spacing and sizing constants.
/// Add more sizes here as the app grows.
class AppSizes {
  AppSizes._();

  /// Horizontal padding used across forms and cards
  static const double kHorizontalPadding = 16.0;

  /// Default vertical padding value (used as a base)
  /// Example: kDefaultPadding * 0.9 -> 18.0 when kDefaultPadding is 20.0
  static const double kDefaultPadding = 20.0;

  /// Default border radius for rounded UI elements
  static const double kDefaultBorderRadius = 12.0;
}
